using System;

namespace mycalculator
{
    public class calc
    {
        public static int add(int a, int b)
        {
            return a + b;
        }
        public static int prod(int a, int b)
        {
            return a * b;
        }
        public static int cube(int a)
        {
            return a * a * a;
        }
    }
}
